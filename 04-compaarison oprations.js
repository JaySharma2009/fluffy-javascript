/*

== equal to
!= not equal to
> greater than
< less than
>= greater than or equal to
<=  less than or equal to
=== equal value and equal type
=  assignment operator

*/

console.log("2"==2); //true
console.log("2"===2); //false
console.log("2"!=2); //false
console.log("2"!==2); //true
console.log("2">2); //false
console.log("2"<2); //false
console.log("2">=2); //true
console.log("2"<=2); //true
